Portal sa lektirama Moje Lektire odgovara na vaša najčešća pitanja. Na ovome mjestu možete pronaći većinu informacija vezanih uz lektire i korištenje portala.
Ukoliko imate nekih dodatnih pitanja slobodno nas kontaktirajte putem <a href="<?= site_url("informacije/kontakt"); ?>" title="Kontaktirajte nas">kontakt forme</a>.
</p>

<dl>

<dt>Što su slova na vrhu stranice?</dt>
<dd>
Slova na vrhu stranice predstavljaju početna slova pojedinih lektira ili pisaca te služe radi jednostavnije navigacije portalom Moje Lektire. Odabirom određenog slova dobivate
prikaz grupiranih lektira ili pisaca kojima početno slovo imena, odnosno prezimena kod pisaca, počinje odabranim slovom. Kada odaberete iz izbornika Lektire (na vrhu stranice) 
tada će se prikazati slova za lektire, a kada odaberete iz izbornika Pisci tada će se prikazati slova za pisce. 
</dd>

<dt>Što predstavlja sadržaj na stranici sa određenom lektirom?</dt>
<dd>
 Sadržaj na stranicama lektire portala Moje Lektire je sadržaj koji je uredništvo portala odredilo kao najbitnije i najkvalitetnije informacije o određenoj lektiri. Taj sadržaj ne mora nužno biti najbolji te je često puta skraćen radi preglednosti same stranice.
</dd>

<dt>Što su to Lektire za download?</dt>
<dd>
Lektire za download su PDF datoteke koje sadrže sve relevantne informacije o lektiri kao što je dnevnik čitanja. Prednost lektira za download, odnosno PDF datoteka je u tome što su prigodnije za čitanje i ispis na pisaču. 
Lektire u obliku datoteka ostaju trajno pohranjene na vašem računalu te ih možete koristiti i u slučaju kada nemate mogućnost pristupa Internetu. 
</dd>

<dt>Postoji više lektira za download. Koju lektiru/datoteku da preuzmem?</dt>
<dd>
Svaka datoteka sa lektirom je jedinstvena i sadrži slične, ali ne i iste infromacije o lektiri. Stoga vam preporučujemo preuzimanje svih lektira kao biste ocijenili koje informacije/sadržaj iz
tih datoteka vam najbolje odgovara. Svaki sadržaj datoteka sa lektirom je mišljenje određene osobe koja je napisala lektiru te ujedno takav sadržaj ne mora biti nužno točan ili ne mora biti jednak vašem mišljenju.  
</dd>

<dt>Kako otvoriti preuzetu/skinutu <a class="help" href="http://hr.wikipedia.org/wiki/Portable_Document_Format" title="Wikipedia: Portable Document Format">PDF</a> datoteku sa lektirom?</dt>
<dd>
Datoteke za sve lektire definirane su u PDF formatu. Da bi ste ih otvorili/pročitali potrebno je posjedovati aplikaciju za čitanje PDF datoteka. Ukoliko nemate aplikaciju za čitanje PDF datoteka tada možete besplatno preuzeti <a href="http://get.adobe.com/reader/"> Adobe Acrobat Reader</a> aplikaciju.
</dd>


<dt>Što su to Online lektire?</dt>
<dd>
Online lektire su lektire koje su besplatno dostupne na drugim Internetskim stranicama. Portal Moje Lektire je za vas na jednome mjestu prikupio srodne 
Internetske stranice za pojedine lektire kako ne biste trebali uokolo tražiti dodatne resurse.
</dd>

<dt>Kada koristiti sadržaj na stranicama lektire portala Moje Lektire, kada preuzeti PDF datoteku, a kada koristiti sadržaj sa Online lektira?</dt>
<dd>
Najjednostavniji odgovor bi bio svaki puta kada tražite određenu lektiru. Sadržaj na stranicama lektire portala Moje Lektire je sadržaj koji je uredništvo portala Moje Lektire
odredilo kao najbitnije i najkvalitetnije informacije o određenoj lektiri. Taj sadržaj ne mora nužno biti najbolji te je često puta skraćen radi preglednosti same stranice. Iz
tog razloga preporučujemo da preuzmete većinu lektira za download i koristite Online lektire kako bi pronašli za vas relevantne informacije o određenoj lektiri.
</dd>

<dt>Gdje se nalaze Lektire za download i Online lektire?</dt>
<dd>
Lektire za download i Online lektire se nalaze u desnom stupcu stranice sa traženom lektirom.
</dd>

<dt>Imam lektiru koju sam napisao/la, da li ju možete objaviti?</dt>
<dd>
Naravno. Rado ćemo objaviti kvalitetne i nove lektire. Pošaljite nam vašu lektiru preko <a href="<?= site_url("lektire/upload"); ?>" title="Pošaljite nam lektiru">forme za učitavanje lektira</a>. 
</dd>

<dt>Zašto da pošaljem svoju lektiru? Dovoljno sam se namučio/la da ju pročitam i napišem.</dt>
<dd>
Da pomognete drugima, budete cool i popularni na Internetu, barem što se lektire tiče. Mnogi školarci su pročitali lektiru ali se ne snalaze dovoljno dobro sa pitanjima za lektiru, ili im nije dovoljno jasno što trebaju napraviti. Stoga ukoliko
pogledaju neku tuđu lektiru biti će im jasnije što i kako napraviti. Naravno time ćete pomoći i onim nešto lijenijim školarcima kojima se jednostavno neda čitati lektiru, pa budite prema njima kolegijalni.  
</dd>


